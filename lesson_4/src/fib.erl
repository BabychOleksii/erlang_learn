%====================================================
% ===================== Task ========================
%====================================================
% Оптимизировать функцию fib из примера с юнит тестами (опционально)


%========================================================
% ===================== Solution ========================
%========================================================
-module(fib).
-export([fib/1]).

fib(0) -> 1;
fib(1) ->1;
fib(N) when N>1 ->
  fib(N, 1, 1).

fib(1, _Prev, Next) ->
  Next;
fib(N, Prev, Next) ->
  fib(N-1, Next, Prev+Next).